package Characters;

public class Equipment {
    private String leftHand;
    private String rightHand;
    private String secondaryWeaponLH;
    private String secondaryWeaponRH;

    public String getLeftHand() {
        return leftHand;
    }

    public String getRightHand() {
        return rightHand;
    }

    public String getSecondaryWeaponLH() {
        return secondaryWeaponLH;
    }

    public String getSecondaryWeaponRH() {
        return secondaryWeaponRH;
    }

    public void setLeftHand(String leftHand) {
        this.leftHand = leftHand;
    }

    public void setRightHand(String rightHand) {
        this.rightHand = rightHand;
    }

    public void setSecondaryWeaponLH(String secondaryWeaponLH) {
        this.secondaryWeaponLH = secondaryWeaponLH;
    }

    public void setSecondaryWeaponRH(String secondaryWeaponRH) {
        this.secondaryWeaponRH = secondaryWeaponRH;
    }
}
